# Die Von-Neumann-Sonde ist ein hypothetisches Konzept für selbstreplizierende Raumschiffe, 
# das auf der Idee der selbstreproduzierenden Automaten des Mathematikers John von Neumann beruht. 
# Es wird in der Science-Fiction verwendet und war Gegenstand spekulativer Betrachtungen über außerirdische Zivilisationen.

# Der Grund für die Namensgebung ist der Titel des Buches Theory of Self-Reproducing Automata 
# von John von Neumann und Arthur W. Burks, wobei von Neumann selbst nicht die Nutzung solcher 
# Automaten in der Weltraumforschung vorschlug. Eine Von-Neumann-Sonde zeichnet sich dadurch aus, 
# dass sie ohne Eingriff eines Menschen eine exakte Kopie ihrer selbst herstellen kann, welche 
# ihrerseits wiederum in der Lage ist, sich exakt zu kopieren. Es handelt sich also um eine Art "Maschinen-Einzeller". 
# Nach diesem Szenario wird eine einzige Von-Neumann-Sonde zum nächsten Sonnensystem gestartet, 
# wo sie mindestens zwei Kopien ihrer selbst erstellt, die weiter in das All vordringen.

# Der amerikanische Physiker Robert A. Freitas Jr. veröffentlichte im Jahre 1980 eine Studie 
# mit dem Titel "A Self-Reproducing Interstellar Probe". Er untersucht darin ein Szenario, 
# bei dem eine Raumsonde eine "SEED" (engl. Saat) genannte Apparatur in ein anderes Sonnensystem bringt. 
# Hier errichtet SEED auf einem kleinen Mond einen "FACTORY" genannten Fabrikkomplex, der dann eine neue 
# SEED-Apparatur und das Antriebssystem hierfür fertigt.

